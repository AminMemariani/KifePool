class AppSpacing {
  // Base spacing unit (8px)
  static const double base = 8.0;

  // Spacing scale
  static const double xs = base * 0.5; // 4px
  static const double sm = base * 1; // 8px
  static const double md = base * 1.5; // 12px
  static const double lg = base * 2; // 16px
  static const double xl = base * 3; // 24px
  static const double xxl = base * 4; // 32px
  static const double xxxl = base * 6; // 48px
  static const double huge = base * 8; // 64px

  // Specific spacing for common use cases
  static const double paddingHorizontal = lg; // 16px
  static const double paddingVertical = md; // 12px
  static const double marginHorizontal = lg; // 16px
  static const double marginVertical = md; // 12px

  // Component specific spacing
  static const double cardPadding = lg; // 16px
  static const double buttonPadding = md; // 12px
  static const double inputPadding = md; // 12px
  static const double listItemPadding = lg; // 16px

  // Border radius
  static const double radiusXs = 4.0;
  static const double radiusSm = 8.0;
  static const double radiusMd = 12.0;
  static const double radiusLg = 16.0;
  static const double radiusXl = 20.0;
  static const double radiusXxl = 24.0;

  // Icon sizes
  static const double iconXs = 16.0;
  static const double iconSm = 20.0;
  static const double iconMd = 24.0;
  static const double iconLg = 32.0;
  static const double iconXl = 40.0;
  static const double iconXxl = 48.0;

  // Button heights
  static const double buttonHeightSm = 32.0;
  static const double buttonHeightMd = 40.0;
  static const double buttonHeightLg = 48.0;
  static const double buttonHeightXl = 56.0;

  // App bar height
  static const double appBarHeight = 56.0;
  static const double bottomNavHeight = 80.0;
}
